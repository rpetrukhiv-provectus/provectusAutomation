package homeworkThree;

public class CheckOddEven {
    public static void main(String[] args) {
        int num = 9;

        if (num % 2 == 0){
            System.out.println("It's even number!");
        }
        else {
            System.out.println("It's odd number");
        }
        System.out.println("BYE!");
    }
}
